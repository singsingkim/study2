param_bounds = {'x1': (-1, 5),  # key 다음에 value 는 튜플형태
                'x2': (0, 4),
                }

def y_function(x1, x2):
    return -x1 **2 - (x2 -2) **2 + 10

# pip install bayesian-optimization
from bayes_opt import BayesianOptimization

# 함수의 최대값을 찾겠다
optimizer = BayesianOptimization(
    f= y_function,
    pbounds=param_bounds,
    random_state=123,
)

optimizer.maximize(init_points = 5,     # 초기 포인트 몇개 찍을래?
                   n_iter=20,           # 25 번 훈련
                   )

print(optimizer.max)

